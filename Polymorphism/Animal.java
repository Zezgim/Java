class Animal
{
    private String name;
    
    public Animal(String name)
    {
        this.name = name;
    }
    
    public String getname()
    {
        return name;
    }
    
    public void setname(String name)
    {
        this.name = name;
    }
    
    public String Speak()
    {
        return "Animal speaks";
    }
}
